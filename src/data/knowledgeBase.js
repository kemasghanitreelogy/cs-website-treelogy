export function getArticlesByCategory(categoryId) {
  return articles.filter((a) => a.categoryId === categoryId);
}

export function searchArticles(query, lang = "id") {
  const q = query.toLowerCase();
  return articles.filter((a) => {
    const questionMatch =
      a.question[lang]?.toLowerCase().includes(q) ||
      a.question.id?.toLowerCase().includes(q) ||
      a.question.en?.toLowerCase().includes(q);
    const answerMatch =
      a.answer[lang]?.toLowerCase().includes(q) ||
      a.answer.id?.toLowerCase().includes(q) ||
      a.answer.en?.toLowerCase().includes(q);
    return questionMatch || answerMatch;
  });
}

